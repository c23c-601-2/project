package com.c23c_601_2.dtoITboard;

public class StudyBoardDTO {
	int studyboard_no, studyboard_count;
	String studyboard_title, studyboard_write, studyboard_date;
	public int getStudyboard_no() {
		return studyboard_no;
	}
	public void setStudyboard_no(int studyboard_no) {
		this.studyboard_no = studyboard_no;
	}
	public int getStudyboard_count() {
		return studyboard_count;
	}
	public void setStudyboard_count(int studyboard_count) {
		this.studyboard_count = studyboard_count;
	}
	public String getStudyboard_title() {
		return studyboard_title;
	}
	public void setStudyboard_title(String studyboard_title) {
		this.studyboard_title = studyboard_title;
	}
	public String getStudyboard_write() {
		return studyboard_write;
	}
	public void setStudyboard_write(String studyboard_write) {
		this.studyboard_write = studyboard_write;
	}
	public String getStudyboard_date() {
		return studyboard_date;
	}
	public void setStudyboard_date(String studyboard_date) {
		this.studyboard_date = studyboard_date;
	}
	
	
	
}
